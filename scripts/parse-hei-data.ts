function parseHEIData(data: string) {
  const lines = data.trim().split('\n');
  const header = lines[0];
  const records = lines.slice(1);
  
  return records.map(line => {
    const parts = line.split('\t');
    return {
      region: parts[0]?.trim() || '',
      heiName: parts[1]?.trim() || '',
      heiType: parts[2]?.trim() || ''
    };
  }).filter(record => record.region && record.heiName && record.heiType);
}

