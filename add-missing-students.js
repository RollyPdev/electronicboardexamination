async function addMissingStudents() {
  try {
    console.log('Adding missing students...');
    
    for (const student of missingStudents) {
      await prisma.student.create({
        data: student
      });
      console.log(`Added student: ${student.firstName} ${student.lastName} (${student.studentId})`);
    }
    
    console.log(`Successfully added ${missingStudents.length} missing students!`);
    
    // Verify total count
    const totalStudents = await prisma.student.count();
    console.log(`Total students in database: ${totalStudents}`);
    
  } catch (error) {
    console.error('Error adding students:', error);
  } finally {
    await prisma.$disconnect();
  }
}

